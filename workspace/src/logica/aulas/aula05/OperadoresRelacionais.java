package logica.aulas.aula05;

public class OperadoresRelacionais {

	public static void main(String[] args) {

		System.out.println(6 < 14); // menor

		System.out.println(6 <= 14); // menor ou igual

		System.out.println(6 > 14); //maior

		System.out.println(6 >= 14); //maior ou iguaç
		
		int num1 =40;
		System.out.println(num1 != 14); //operador de diferença
		
		System.out.println(num1 == 14); //operador de atribuição
		
		int num2 = 31;
		
		boolean comparacao = num1 < num2;
		System.out.println(comparacao);
		
		
	}

}
