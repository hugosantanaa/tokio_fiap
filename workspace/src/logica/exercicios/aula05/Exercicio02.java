package logica.exercicios.aula05;

public class Exercicio02 {

	public static void main(String[] args) {
		
		int var1 = 5;
		float var2 =6f;
		double var3 =8.0;
		
		System.out.println(var1+"\n"+var2+"\n"+var3+"\n");
		
		String varInt = Integer.toString(var1);
		String varFloat = Float.toString(var2);
		String varDouble = Double.toString(var3);
		
		System.out.println((varInt+2)+"\n"+(varFloat+3)+"\n"+(varDouble+4));
		

	}

}
